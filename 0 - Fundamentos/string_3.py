nome = "Juliana Catle da Silva"

print(nome[0])                  # pega a primeira
print(nome[-2])                 # conta do J pra tras
print(nome[:9])                 # mostra as 9 primeiras
print(nome[10:])                # mostra as 10 ultimas
print(nome[10:16])              # mostra da 11 a 16
print(nome[10:16:2])            # mostra da 11 a 16 pulando uma letra
print(nome[:])                  # mostra tudo
print(nome[::-1])               # mostra invertido
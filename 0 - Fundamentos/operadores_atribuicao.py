saldo = 500
print(saldo)

saldo = 200
print(saldo)

saldo += 5              # Soma 5 ao valor do saldo 
print(saldo)

saldo -= 10             # Subtrai 10 ao valor do saldo
print(saldo)

saldo *= 2              # Multiplica por 2 o valor do saldo
print(saldo)

saldo /= 10             # Divide por 10 o valor do saldo
print(saldo)

saldo //= 3             # Divide por 3 o valor do saldo convertendo em inteiro
print(saldo)

saldo **= 2             # Exponencia por 2 o valor do saldo
print(saldo)

saldo %= 5              # Divide por 5 o valor do saldo e mostra o resto
print(saldo)
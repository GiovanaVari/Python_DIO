saldo = 200
saque = 200

print(saldo == saque)           # Valor de saldo é igual ao valor de saque?
print(saldo != saque)           # Valor de saldo é diferente do valor de saque?
print(saldo > saque)            # Valor de saldo é maior que valor de saque?
print(saldo >= saque)           # Valor de saldo é maior igual ao valor de saque?
print(saldo < saque)            # Valor de saldo é menor que valor de saque?
print(saldo <= saque)           # Valor de saldo é menor igual ao valor de saque?
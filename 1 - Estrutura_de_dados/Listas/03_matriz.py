matriz = [
    [1, "a", 2],
    ["b", 3, 4],
    [6, 5, "c"]
]

print(matriz[0])                                    # Mostra o que esta na posição L0 da matriz ([1, "a", 2])
print(matriz[0][0])                                 # Mostra o que esta na posição L0 C0 da matriz (1)
print(matriz[0][-1])                                # Mostra o que esta na posição L0 C-1 da matriz (2)
print(matriz[-1][-1])                               # Mostra o que esta na posição L-1 C-1 da matriz ("c")
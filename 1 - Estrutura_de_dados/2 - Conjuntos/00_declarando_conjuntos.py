numeros = set([1, 2, 3, 1, 3, 4])                           # set - é uma coleção que não possui objetos repetidos
print(numeros)                                              # Mostra o set          {1, 2, 3, 4}

letras = set("abacaxi")
print(letras)                                               # Mostra o set          {"b", "a", "c", "x", "i"}

carros = set(("palio", "gol", "celta", "palio"))
print(carros)                                               # Mostra o set          {"gol", "celta", "palio"}